<h2>Thalassothérapie, qu'est-ce que c'est ?</h2>

<p>Une cure de thalassothérapie se déroule dans un institut en bord de mer. La thalassothérapie est basée sur les bienfaits de l'eau de mer et de l'environnement marin riche en sels minéraux comme les algues, la boue marine et l'air marin. En effet, l'air marin ainsi que les paysages relaxants en bord de mer participent aux bienfaits de la cure.

Les soins thalasso s'appuient sur les principes de l'hydrothérapie. Ils peuvent être préventifs ou curatifs.

La température de l'eau de mer est aussi un élément essentiel de la thalassothérapie. L'eau chauffée entre 31°C et 35°C permet de dilater les pores de la peau pour que les oligo-éléments puissent circuler dans l'organisme.

Une thalassothérapie comprend minimum 3 soins par jour et devrait idéalement s'étendre sur 6 jours pour obtenir une efficacité maximale sur le corps et l'esprit.</p>

<h2>Les bienfaits d'une thalassotherapie</h2>
<p>La thalassothérapie permet de prendre soin de son corps et de son esprit dans un environnement propice à la détente. Les oligo-éléments et les sels minéraux de l'eau de mer revitalisent le corps et éliminent ses toxines. L'air marin, riche en ions négatifs et en oligo-éléments, participe au bien-être des personnes ayant des problèmes respiratoires ou de stress.</p>

<h2>À qui s'adresse la thalassothérapie ?</h2>
<p>La thalassothérapie s'adresse principalement aux adultes.

Les cures de thalassothérapie sont spécialisées en fonction des besoins de la personne. On trouve alors des thalassothérapies pour : les femmes enceintes, les personnes âgées, les personnes qui ont des troubles du sommeil, les personnes qui ont une mauvaise hygiène alimentaire, les personnes qui souffrent d'un stress important. Les enfants ne peuvent suivre que certains soins en thalassothérapie. Parlez-en à votre médecin.</p>
<h2>Thalassothérapie : vacances ou thérapie ?</h2>
<p>Dans le cas d'une cure de thalassothérapie, on préférera un sejour thalasso long (une semaine). Dans le cas d'une thalassothérapie de bien-être, ce pourra être un week-end thalasso ou encore, à l'étranger, un voyage thalasso, pour allier tourisme et soin.</p>


